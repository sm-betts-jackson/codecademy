def nth_last_node(linked_list, n):
  linked_list_as_list = []
  current_node = linked_list.head_node
  while current_node:
    linked_list_as_list.append(current_node)
    current_node = current_node.get_next_node()
  return linked_list_as_list[len(linked_list_as_list) - n]

